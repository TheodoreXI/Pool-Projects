int	ft_sqrt(int nb)
{
	int	i;
	int	half;

	i = 2;
	half = nb / 2;
	if (nb <= 1)
	{
		return (nb);
	}
	while ((nb > 0) && (i <= half))
	{
		if (nb == i * i)
		{
			return (i);
		}
		i++;
	}
	return (0);
}
